select dup.No_Of_Duplicate_Rows from(
select 
EMP_ID,
EMP_NAME,
DEPT_NAME,
MANAGER_NAME,
JOB_TYPE,
LOCATION,
HIRE_DATE,
TOTAL_EXP_IN_COMPANY,
SALARY,
COMMISSION,
TOTAL_SALARY,
SALARY_GRADE,
COUNT(*) as No_Of_Duplicate_Rows
from T_EMP
group by
EMP_ID,
EMP_NAME,
DEPT_NAME,
MANAGER_NAME,
JOB_TYPE,
LOCATION,
HIRE_DATE,
TOTAL_EXP_IN_COMPANY,
SALARY,
COMMISSION,
TOTAL_SALARY,
SALARY_GRADE
having COUNT(*) >1) dup